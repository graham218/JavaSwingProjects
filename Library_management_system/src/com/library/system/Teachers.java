    public void Auto_Id(){
        try {
            Statement s=con.createStatement();
            rs=pst.executeQuery("select MAX(teachersNo) from teachers");
            rs.next();
            rs.getString("MAX(teachersNo)");
            if(rs.getString("MAX(teachersNo)")==null){
                lblTeachersNo.setText("TR001");
            }else{
                long id=Long.parseLong(rs.getString("MAX(teachersNo)").substring(2,rs.getString("MAX(teachersNo)").length()));
                id++;
                lblTeachersNo.setText("TR"+String.format("%03d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Teachers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void teachers_table(){
        try {
            pst=con.prepareStatement("select * from teachers");
            rs=pst.executeQuery();
            ResultSetMetaData rsm=rs.getMetaData();
            int c=rsm.getColumnCount();
            
            DefaultTableModel model=(DefaultTableModel)table.getModel();
            model.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for (int i = 1; i <= c; i++) {
                    v.add(rs.getString("teachersNo"));
                    v.add(rs.getString("nationalId"));
                    v.add(rs.getString("teachersName"));
                    v.add(rs.getString("phone"));
                }
                model.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Teachers.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
