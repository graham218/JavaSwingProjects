    public void Auto_Id(){
        try {
            Statement s=con.createStatement();
            rs=pst.executeQuery("select MAX(studNo) from students");
            rs.next();
            rs.getString("MAX(studNo)");
            if(rs.getString("MAX(studNo)")==null){
                lblStudentId.setText("ST001");
            }else{
                long id=Long.parseLong(rs.getString("MAX(studNo)").substring(2,rs.getString("MAX(studNo)").length()));
                id++;
                lblStudentId.setText("ST"+String.format("%03d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Students.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void student_table(){
        try {
            pst=con.prepareStatement("select * from students");
            rs=pst.executeQuery();
            ResultSetMetaData rsm=rs.getMetaData();
            int c=rsm.getColumnCount();
            
            DefaultTableModel model=(DefaultTableModel)table.getModel();
            model.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for (int i = 1; i <= c; i++) {
                    v.add(rs.getString("studNo"));
                    v.add(rs.getString("admNo"));
                    v.add(rs.getString("studName"));
                    v.add(rs.getString("school"));
                    v.add(rs.getString("class"));
                    v.add(rs.getString("dateAdmitted"));
                    v.add(rs.getString("parentPhone"));
                    v.add(rs.getBlob("profilePhoto"));
                }
                model.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Students.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
