    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        //editBtn.setEnabled(true);
        //deleteBtn.setEnabled(true);
        //addBtn.setEnabled(false);
        
        DefaultTableModel model=(DefaultTableModel)table.getModel();
        int selectedIndex=table.getSelectedRow();
        
        lblStudentId.setText(model.getValueAt(selectedIndex, 0).toString());
        txtAdmissionNo.setText(model.getValueAt(selectedIndex, 1).toString());
        txtSudentName.setText(model.getValueAt(selectedIndex, 2).toString());
        txtSchool.setText(model.getValueAt(selectedIndex, 3).toString());
        txtClass.setText(model.getValueAt(selectedIndex, 4).toString());
        String date=(model.getValueAt(selectedIndex, 5).toString());
        try{
            SimpleDateFormat format=new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date d=format.parse(date);
            txtDateOfAddmission.setDate(d);
        }catch(Exception ex){
            ex.printStackTrace();
        }
        txtParentPhone.setText(model.getValueAt(selectedIndex, 6).toString());
        Blob img=(Blob) model.getValueAt(selectedIndex, 7);
        byte[] imageData;
        try {
            imageData = img.getBytes(1,(int)img.length());
            BufferedImage imag=ImageIO.read(new ByteArrayInputStream(imageData));
            studentPhoto.setIcon(new ImageIcon(new javax.swing.ImageIcon(imag).getImage().getScaledInstance(130, 150, Image.SCALE_SMOOTH)));
        } catch (SQLException ex) {
            Logger.getLogger(Students.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Students.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_tableMouseClicked

