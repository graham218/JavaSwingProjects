    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String datePaid=lblDate.getText();
        String bookNo=lblBookNo.getText();
        String bookName=lblBookName.getText();
        int price=Integer.parseInt(lblPrice.getText());
        String studentName=lblTeachersName.getText();
        String admNo=lblNationalId.getText();
        String confirm=combReturn.getSelectedItem().toString();
        
        if(bookNo.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please select a book from the top right table, null books cannot be returned","Submission Error",JOptionPane.CANCEL_OPTION);
            txtSearch.requestFocus();
        }else if(combReturn.getSelectedItem().toString().trim().equals("CONFIRM BOOK PAYMENT")){
            JOptionPane.showMessageDialog(this, "Please confirm if the book is paid or not, null confirmations cannot be submitted","Submission Error",JOptionPane.CANCEL_OPTION);
            combReturn.requestFocus();
        }else{
            try {
                pst=con.prepareStatement("insert into paidTeachers (datePaid,bookNo,bookName,price,teachersName,nationalId,confirmation) values(?,?,?,?,?,?,?)");
                pst.setString(1, datePaid);
                pst.setString(2, bookNo);
                pst.setString(3, bookName);
                pst.setInt(4, price);
                pst.setString(5, studentName);
                pst.setString(6, admNo);
                pst.setString(7, confirm);
                pst.executeUpdate();
                if(combReturn.getSelectedItem().toString().trim().equals("PAID")){
                    pst=con.prepareStatement("delete from returnedTeachersBooks where bookNo=?");
                    pst.setString(1, bookNo);
                    pst.executeUpdate();
                }
                JOptionPane.showMessageDialog(this,"Book payment confirmed successfully, please assign the new book a new number and enter it into the system","Success Message",JOptionPane.INFORMATION_MESSAGE);
                lblBookNo.setText("");
                lblBookName.setText("");
                lblPrice.setText("");
                lblTeachersName.setText("");
                lblNationalId.setText("");
                lblPhoneNumber.setText("");
                combReturn.getModel().setSelectedItem("CONFIRM BOOK RETURN");
                current_date();
                returned_books_table();
                    } catch (SQLException ex) {
                Logger.getLogger(LostBooksTeachers.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

