    public void returned_books_table(){
        try {
            pst=con.prepareStatement("select * from returnedTeachersBooks where confirmation='LOST'");
            rs=pst.executeQuery();
            ResultSetMetaData rsm=rs.getMetaData();
            int c=rsm.getColumnCount();
            
            DefaultTableModel model=(DefaultTableModel)table.getModel();
            model.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for (int i = 1; i <= c; i++) {      
                    v.add(rs.getString("dateReturned"));
                    v.add(rs.getString("bookNo"));
                    v.add(rs.getString("bookName"));
                    v.add(rs.getInt("price"));
                    v.add(rs.getString("teachersName"));
                    v.add(rs.getString("nationalId"));
                    v.add(rs.getString("phone"));
                    v.add(rs.getString("confirmation"));
                }
                model.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(IssueBooks.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
