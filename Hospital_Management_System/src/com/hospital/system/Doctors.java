    public void Auto_Id(){
        try {
            Statement s=con.createStatement();
            rs=s.executeQuery("select MAX(customerNo) from doctors");
            rs.next();
            rs.getString("MAX(doctorNo)");
            if(rs.getString("MAX(doctorNo)")==null){
                lblCustomerNo.setText("DR0001");
            }else{
                long id=Long.parseLong(rs.getString("MAX(doctorNo)").substring(2,rs.getString("MAX(doctorNo)").length()));
                id++;
                lblCustomerNo.setText("DR"+String.format("%04d", id));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Doctors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
