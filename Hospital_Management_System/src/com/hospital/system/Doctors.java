    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String customerNo=lblCustomerNo.getText();
        String nationalId=txtNationalId.getText();
        String name=txtName.getText();
        String phone=txtPhoneNo.getText();
        String specialisation=txtSpecialisation.getText();
        
        if(nationalId.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter doctor's national ID, null IDs cannot be submitted","Submission Error",JOptionPane.CANCEL_OPTION);
            txtNationalId.requestFocus();
        }else if(name.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter doctor's name, null names cannot be submitted","Submission Error",JOptionPane.CANCEL_OPTION);
            txtName.requestFocus();
        }else if(phone.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter phone number, null phone numbers cannot be submitted","Submission Error",JOptionPane.CANCEL_OPTION);
            txtPhoneNo.requestFocus();
        }else if(specialisation.trim().equals("")){
            JOptionPane.showMessageDialog(this, "Please enter doctor's specialisation , null specialisations cannot be submitted","Submission Error",JOptionPane.CANCEL_OPTION);
            txtSpecialisation.requestFocus();
        }else{
            byte[] rawBytes = null;  
            FileInputStream fis = null; 
            
            try {
                File fileObj = new File(path1);
                fis = new FileInputStream(fileObj);
                
                pst=con.prepareStatement("insert into doctors (doctorNo,nationalId,doctorName,phone,specialisation,profilePic) values(?,?,?,?,?,?,?)");
                
                int imageLength = Integer.parseInt(String.valueOf(fileObj.length()));  
                rawBytes = new byte[imageLength];
                fis.read(rawBytes, 0, imageLength);
                pst.setString(1,customerNo);
                pst.setString(2,nationalId);             
                pst.setString(3,name);
                pst.setString(4,phone);
                pst.setString(5,specialisation);
                pst.setBytes(6, rawBytes);
                int count = pst.executeUpdate();
                if (count > 0) {
                    JOptionPane.showMessageDialog(this, "Doctor Added Successfully into database","User Creation",JOptionPane.INFORMATION_MESSAGE);
                    Auto_Id();
                    txtNationalId.setText("");
                    txtName.setText("");
                    txtSpecialisation.setText("");
                    txtPhoneNo.setText("");
                    lblUserPhoto.setIcon(null);
                    txtNationalId.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "An Error Occured while trying to send your details into the database, please try again","Error Message",JOptionPane.CANCEL_OPTION);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Doctors.class.getName()).log(Level.SEVERE, null, ex);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Doctors.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Doctors.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_addBtnActionPerformed

